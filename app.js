//ejs and view engine:
app.set("views", path.join(__dirname, "views"))
app.set("view engine", "ejs")

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Middleware for static files:
app.use(express.static("public"))



